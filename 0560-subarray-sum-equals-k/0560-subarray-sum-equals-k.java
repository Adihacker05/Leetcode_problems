import java.util.HashMap;

class Solution {
    public int subarraySum(int[] nums, int k) {
        HashMap<Integer, Integer> map = new HashMap<>();
        
        map.put(0, 1);  // VERY IMPORTANT
        
        int sum = 0;
        int count = 0;
        
        for (int num : nums) {
            sum += num;

            int rem = sum - k;

            if (map.containsKey(rem)) {
                count += map.get(rem);
            }

            map.put(sum, map.getOrDefault(sum, 0) + 1);
        }
        
        return count;
    }
}
